#    Use:
#    decrypt("01001000 01100101 01101100 01101100 01101111 00100000 01010111 01101111 01110010 01101100 01100100 00100001")
#    => "Hello World!"
#
# ==============================================================================

def decrypt(s):

	output = ""

	for b in s.split(' '):
		output += chr(int(b, 2))

	return output